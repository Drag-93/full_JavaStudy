package org.java.basic.varEx;

public class VarEx1 {

	public static void main(String[] args) {

		System.out.println("변수");

		// int형(4byte)의 데이터를 저장할수 있는 변수 i를 선언했다.
		int i;
		// 변수 i를 10으로 초기화 했다.
		i = 10;

		System.out.println(i);
		
		// int형의 데이터를 저장할수 있는 변수 j를 선언하고 
		// int형 10으로 초기화 했다.
		int j=10;
		System.out.println("j-> "+j);
		System.out.println(j+"+"+10+"="+(j+10));
		System.out.println(j+"*"+10+"="+(j*10));

	}
}
